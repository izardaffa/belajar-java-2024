public class operator {
    public static void main(String[] args) {
        int x = 12;
        int y = 2;

        System.out.println(x + y);  // output 14
        System.out.println(x - y);  // output 10
        System.out.println(x * y);  // output 24
        System.out.println(x / y);  // output 6
        System.out.println(x % y);  // output 0

        // Increment (++)
        // jika x++, x akan ditambah satu setelah operasi dijalankan, output 12
        // jika ++x, x akan ditambah satu saat operasi dijalankan, output 13

        // Decrement (--)
        // jika x--, x akan dikurangi satu setelah operasi dijalankan, output 12
        // jika --x, x akan dikurangi satu saat operasi dijalankan, output 11

        // Assignment Operators
        // Comparsion Operators
        // sama seperti di javascript (tau lah 😝)

        // Logical Operators
        // && => and
        // || => or
        // ! => not
    }
}
